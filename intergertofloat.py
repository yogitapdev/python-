integer_value = 10
float_value = float(integer_value)

print(f"Original integer: {integer_value}")
print(f"Converted float: {float_value}")


result = 10 / 3  # Integer division (truncates)
print(f"Integer division: {result}")  # Output: 3

result = 10.0 / 3  # Float division
print(f"Float division: {result}")  # Output: 3.3333333333333335
