import math

# Example 1: ceil(x) - Rounding up to nearest integer
number = 3.14
ceiling = math.ceil(number)
print(f"The ceiling of {number} is: {ceiling}")  # Output: The ceiling of 3.14 is: 4

# Example 2: fabs(x) - Absolute value
number = -5.2
absolute_value = math.fabs(number)
print(f"The absolute value of {number} is: {absolute_value}")  # Output: The absolute value of -5.2 is: 5.2
